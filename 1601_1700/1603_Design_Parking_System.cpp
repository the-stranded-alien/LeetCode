// Implement the ParkingSystem class:

// ParkingSystem(int big, int medium, int small) Initializes object of the ParkingSystem class. The number of slots for each parking space are given as part of the constructor.
// bool addCar(int carType) Checks whether there is a parking space of carType for the car that wants to get into the parking lot. carType can be of three kinds: big,
// medium, or small, which are represented by 1, 2, and 3 respectively. A car can only park in a parking space of its carType. If there is no space available, return false, else
// park the car in that size space and return true.

class ParkingSystem {
private:
    int big;
    int medium;
    int small;
public:
    ParkingSystem(int big, int medium, int small) {
        this->big = big;
        this->medium = medium;
        this->small = small;
    }
    
    bool addCar(int carType) {
        if(carType == 1) {
            if(big >= 1) {
                big -= 1;
                return true;
            } else return false;
        } else if(carType == 2) {
            if(medium >= 1) {
                medium -= 1;
                return true;
            } else return false;
        } else {
            if(small >= 1) {
                small -= 1;
                return true;
            } else return false;
        }
    }
};

