// A newly designed keypad was tested, where a tester pressed a sequence of n keys, one at a time.

// You are given a string keysPressed of length n, where keysPressed[i] was the ith key pressed in the testing sequence, and a sorted list releaseTimes, where releaseTimes[i] was the time the ith key was released. Both arrays are 0-indexed. The 0th key was pressed at the time 0, and every subsequent key was pressed at the exact time the previous key was released.

// The tester wants to know the key of the keypress that had the longest duration. The ith keypress had a duration of releaseTimes[i] - releaseTimes[i - 1], and the 0th keypress had a duration of releaseTimes[0].

// Note that the same key could have been pressed multiple times during the test, and these multiple presses of the same key may not have had the same duration.

// Return the key of the keypress that had the longest duration. If there are multiple such keypresses, return the lexicographically largest key of the keypresses.

class Solution {
public:
	char slowestKey(vector<int>& releaseTimes, string keysPressed) {
        int rt = releaseTimes[0];
        char c = keysPressed[0];
        for(int i = 1; i < releaseTimes.size(); i++) {
        	int rtc = releaseTimes[i] - releaseTimes[i - 1];
        	if(rtc > rt) {
        		rt = rtc;
        		c = keysPressed[i];
        	} 
        	else if(rtc == rt) {
        		if(keysPressed[i] > c) {
        			c = keysPressed[i];
        		}
        	}
        }
        return c;
    }
};

void solve() {
	// Main Code Goes Here !!
	int n; cin >> n;
	vector<int> releaseTimes(n);
	for(int i = 0; i < n; i++) cin >> releaseTimes[i];
	string keysPressed; cin >> keysPressed;
	
	Solution *soln = new Solution();
	cout << soln->slowestKey(releaseTimes, keysPressed);

	return;
}

