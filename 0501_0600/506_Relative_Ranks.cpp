// You are given an integer array score of size n, where score[i] is the score of the ith athlete in a competition. All the scores are guaranteed to be unique.
// The athletes are placed based on their scores, where the 1st place athlete has the highest score, the 2nd place athlete has the 2nd highest score, and so on. The placement of each athlete determines their rank:
// The 1st place athlete's rank is "Gold Medal".
// The 2nd place athlete's rank is "Silver Medal".
// The 3rd place athlete's rank is "Bronze Medal".
// For the 4th place to the nth place athlete, their rank is their placement number (i.e., the xth place athlete's rank is "x").
// Return an array answer of size n where answer[i] is the rank of the ith athlete.

class Solution {
public:
    vector<string> findRelativeRanks(vector<int>& score) {
		int n = score.size();
		priority_queue< pair<int, int> > rankPQ;
		vector<string> relativeRanks(n, "");
		for(int i = 0; i < n; i++) rankPQ.push(make_pair(score[i], i));
		int rank = 1;
		while(!rankPQ.empty()) {
			pair<int, int> curPair = rankPQ.top();
			int scr = curPair.first;
			int idx = curPair.second;
			if(rank > 3) relativeRanks[idx] = to_string(rank);
			else if(rank == 3) relativeRanks[idx] = "Bronze Medal";
			else if(rank == 2) relativeRanks[idx] = "Silver Medal";
			else relativeRanks[idx] = "Gold Medal";
			rank++; 
			rankPQ.pop();
		}
		return relativeRanks;
    }
};

void solve() {
	// Main Code Goes Here !!
	int n; cin >> n;
	vi score(n, 0);
	for(int i = 0; i < n; i++) cin >> score[i];
	Solution *soln = new Solution();
	vs relativeRanks = soln->findRelativeRanks(score);
	for(auto rr : relativeRanks) cout << rr << " ";
	return;
}

