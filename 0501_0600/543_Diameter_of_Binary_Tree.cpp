// Given the root of a binary tree, return the length of the diameter of the tree.
// The diameter of a binary tree is the length of the longest path between any two nodes in a tree. This path may or may not pass through the root.
// The length of a path between two nodes is represented by the number of edges between them.

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */

class Solution {
public:
    pair<int, int> diameterOfBinaryTreeHelper(TreeNode* root) {
        pair<int, int> hd; // {height, diameter}
        if(root == nullptr) {
            hd.first = hd.second = 0;
            return hd;
        }
        pair<int, int> left = diameterOfBinaryTreeHelper(root->left);
        pair<int, int> right = diameterOfBinaryTreeHelper(root->right);
        hd.first = max(left.first, right.first) + 1;
        int d1 = left.first + right.first;
        int d2 = left.second;
        int d3 = right.second;
        hd.second = max(d1, max(d2, d3));
        return hd;
    }
    int diameterOfBinaryTree(TreeNode* root) {
        auto hd = diameterOfBinaryTreeHelper(root);
        return hd.second;
    }
};

