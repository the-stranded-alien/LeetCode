// You are given an integer mass, which represents the original mass of a
// planet. You are further given an integer array asteroids, where
// asteroids[i] is the mass of the ith asteroid.
// You can arrange for the planet to collide with the asteroids in any
// arbitrary order. If the mass of the planet is greater than or equal
// to the mass of the asteroid, the asteroid is destroyed and the planet
// gains the mass of the asteroid. Otherwise, the planet is destroyed.
// Return true if all asteroids can be destroyed. Otherwise, return false.

class Solution {
public:
    bool asteroidsDestroyed(int mass, vector<int>& asteroids) {
        sort(asteroids.begin(), asteroids.end());
        // long long int32_t mass;
        for(int i = 0; i < asteroids.size(); i++) {
        	if(mass < asteroids[i]) return false;
        	else mass += asteroids[i];
        }
        return true;
    }
};

void solve() {
	// Main Code Goes Here !!
	int mass; cin >> mass;
	int ast; cin >> ast;
	vi asteroids(ast, 0);
	for(int i = 0; i < ast; i++) cin >> asteroids[i];
	Solution *soln = new Solution();
	bool canDestroy = soln->asteroidsDestroyed(mass, asteroids);
	if(canDestroy) cout << "true" << nl;
	else cout << "false" << nl;
	return;	
}

