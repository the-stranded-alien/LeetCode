// You are given a string num representing a large integer.
// An integer is good if it meets the following conditions:
// It is a substring of num with length 3.
// It consists of only one unique digit.
// Return the maximum good integer as a string or an empty
// string "" if no such integer exists.
// Note:
// A substring is a contiguous sequence of characters within
// a string. There may be leading zeroes in num or a good integer.

class Solution {
public:
	string largestGoodInteger(string num) {
        int l = num.length();
        char mxCh = '0';
        string ans = "";
        for(int i = 0; i <= (l - 3); i++) {
        	char c1 = num[i];
        	char c2 = num[i + 1];
        	char c3 = num[i + 2];
        	if(c1 == c2 && c2 == c3 && c3 == c1) {
        		if(c1 >= mxCh) {
        			ans = "";
        			mxCh = c1;
        			ans += mxCh;
        			ans += mxCh;
        			ans += mxCh;
        		}
        	}
        }
        return ans;
    }
};

void solve() {
	// Main Code Goes Here !!
	string num; cin >> num;
	
	Solution *soln = new Solution();
	cout << soln->largestGoodInteger(num);

	return;
}

