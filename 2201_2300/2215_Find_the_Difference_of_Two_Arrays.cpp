// Given two 0-indexed integer arrays nums1 and nums2, return a list answer of size 2 where:
// answer[0] is a list of all distinct integers in nums1 which are not present in nums2.
// answer[1] is a list of all distinct integers in nums2 which are not present in nums1.
// Note that the integers in the lists may be returned in any order.

class Solution {
public:
	vector<vector<int>> findDifference(vector<int>& nums1, vector<int>& nums2) {
        set<int> s1(nums1.begin(), nums1.end());
        set<int> s2(nums2.begin(), nums2.end());

        set<int> intersection;
        set_intersection(s1.begin(), s1.end(), s2.begin(), s2.end(), inserter(intersection, intersection.begin()));

        vector<int> v1;
        set_difference(s1.begin(), s1.end(), intersection.begin(), intersection.end(), inserter(v1, v1.begin()));

        vector<int> v2;
        set_difference(s2.begin(), s2.end(), intersection.begin(), intersection.end(), inserter(v2, v2.begin()));
    
        return {v1, v2};
    }
};

