// You are playing a game that has n levels numbered from 0 to n - 1. You are
// given a 0-indexed integer array damage where damage[i] is the amount of health
// you will lose to complete the ith level. You are also given an integer armor.
// You may use your armor ability at most once during the game on any level which
// will protect you from at most armor damage. You must complete the levels in
// order and your health must be greater than 0 at all times to beat the game.
// Return the minimum health you need to start with to beat the game.

class Solution {
public:
    long long minimumHealth(vector<int>& damage, int armor) {
    	return (accumulate(damage.begin(), damage.end(), 0LL) -
            min(*max_element(damage.begin(), damage.end()), armor) + 1);
    }
};

void solve() {
	// Main Code Goes Here !!
	int d; cin >> d;
	vector<int> damage(d, 0);
	for(int i = 0; i < d; i++) cin >> damage[i];
	int armor; cin >> armor;
	Solution *soln = new Solution();
	cout << soln->minimumHealth(damage, armor) << nl;
	return;
}

