// You have some number of sticks with positive integer lengths. These lengths are given as an
// array sticks, where sticks[i] is the length of the ith stick. You can connect any two sticks
// of lengths x and y into one stick by paying a cost of x + y. You must connect all the sticks
// until there is only one stick remaining. Return the minimum cost of connecting all the given
// sticks into one stick in this way.

class Solution {
public:
    int connectSticks(vector<int>& sticks) {
        priority_queue<int, vector<int>, greater<int> > minHeap(sticks.begin(), sticks.end());
        int minCost = 0;
        while(minHeap.size() > 1) {
        	int min1 = minHeap.top();
        	minHeap.pop();
        	int min2 = minHeap.top();
        	minHeap.pop();
        	minCost += (min1 + min2);
        	minHeap.push(min1 + min2);
        }
        return minCost;
    }
};

void solve() {
	// Main Code Goes Here !!
	int s; cin >> s;
	vector<int> sticks(s, 0);
	for(int i = 0; i < s; i++) cin >> sticks[i];
	Solution *soln = new Solution();
	int minCost = soln->connectSticks(sticks);
	cout << minCost << nl;
	return;	
}

