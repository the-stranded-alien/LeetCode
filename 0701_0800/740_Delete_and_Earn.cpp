// You are given an integer array nums. You want to maximize the
// number of points you get by performing the following operation
// any number of times: Pick any nums[i] and delete it to earn
// nums[i] points. Afterwards, you must delete every element equal
// to nums[i] - 1 and every element equal to nums[i] + 1.
// Return the maximum number of points you can earn by applying
// the above operation some number of times.

class Solution {
public:
    int deleteAndEarn(vector<int>& nums) {
    	int n = nums.size();
    	if(n == 0) return 0;
    	else if(n == 1) return nums[n - 1];
    	else {
    		vector<int> freq(20005, 0);
    		vector<int> sum(10005, 0);
    		int maxVal = 0;
    		for(auto num : nums) {
    			freq[num]++;
    			if(num > maxVal) maxVal = num;
    		}
    		sum[0] = freq[0];
    		sum[1] = freq[1];
    		for(int i = 2; i <= maxVal; i++) {
    			sum[i] = max((sum[i - 2] + (freq[i] * i)), sum[i - 1]);
    		}
    		return sum[maxVal];
    	}
    }
};

void solve() {
	// Main Code Goes Here !!
	int n; cin >> n;
	vi nums(n, 0);
	for(int i = 0; i < n; i++) cin >> nums[i];
	Solution *soln = new Solution();
	int maxEarn = soln->deleteAndEarn(nums);
	cout << maxEarn << nl;
	return;
}

