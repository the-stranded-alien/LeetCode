// Given a 1-indexed array of integers numbers that is already sorted in
// non-decreasing order, find two numbers such that they add up to a specific
// target number. Let these two numbers be numbers[index1] and numbers[index2]
// where 1 <= index1 < index2 <= numbers.length.
// Return the indices of the two numbers, index1 and index2, added by one as
// an integer array [index1, index2] of length 2.
// The tests are generated such that there is exactly one solution. You may not
// use the same element twice.

class Solution {
public:
    vector<int> twoSum(vector<int>& numbers, int target) {
        int n = numbers.size();
        int startItr = 0;
        int endItr = (n - 1);
        while(startItr < endItr) {
        	int n1 = numbers[startItr];
        	int n2 = numbers[endItr];
        	if((n1 + n2) == target) return {(startItr + 1), (endItr + 1)};
        	else if((n1 + n2) < target) startItr++;
        	else endItr--;
        }
        return {};
    }
};

void solve() {
	// Main Code Goes Here !!
	int n; cin >> n;
	vi nums(n, 0);
	for(int i = 0; i < n; i++) cin >> nums[i];
	int target; cin >> target;
	Solution *soln =  new Solution();
	vi twoSumPair = soln->twoSum(nums, target);
	for(auto num : twoSumPair) cout << num << " ";
	return;	
}

