// Given two integers n and k, return all possible
// combinations of k numbers out of the range [1, n].
// You may return the answer in any order.

class Solution {
public:
	void combinationHelper(int n, int k, int idx, vector<int> &cur, vector<vector<int>> &combinations, int itr, int last) {
		if(idx == k) {
			combinations.push_back(cur);
			return;
		}
		for(int i = itr; i <= n; i++) {
			if(i > last) {
				cur.push_back(i);
				combinationHelper(n, k, idx + 1, cur, combinations, itr + 1, i);
				cur.pop_back();	
			} 
			else continue;
		}
		return;
	}

    vector<vector<int>> combine(int n, int k) {
        vector<vector<int>> combinations;
        vector<int> cur;
        combinationHelper(n, k, 0, cur, combinations, 1, 0);
        return combinations;
    }
};

void solve() {
	// Main Code Goes Here !!
	int n; cin >> n;
	int k; cin >> k;
	Solution *soln = new Solution();
	vvi combinations = soln->combine(n, k);	
	for(auto combination : combinations) {
		for(auto element : combination) {
			cout << element << " ";
		}
		cout << nl;
	}
	return;
}

