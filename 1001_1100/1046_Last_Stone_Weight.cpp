// You are given an array of integers stones where stones[i] is the weight of the ith stone.
// We are playing a game with the stones. On each turn, we choose the heaviest
// two stones and smash them together. Suppose the heaviest two stones have weights x and y with x <= y. The result of this smash is:
// If x == y, both stones are destroyed, and
// If x != y, the stone of weight x is destroyed, and the stone of weight y has new weight y - x.
// At the end of the game, there is at most one stone left.
// Return the smallest possible weight of the left stone. If there are no stones left, return 0.

class Solution {
public:
    int lastStoneWeight(vector<int>& stones) {
        priority_queue<int> maxHeap(stones.begin(), stones.end());
        while(maxHeap.size() >= 2) {
        	int s1 = maxHeap.top();
        	maxHeap.pop();
        	int s2 = maxHeap.top();
        	maxHeap.pop();
        	if(s1 != s2) maxHeap.push(abs(s1 - s2));
        }
        return (maxHeap.size() == 0 ? 0 : maxHeap.top());
    }
};

void solve() {
	// Main Code Goes Here !!
	int s; cin >> s;
	vector<int> stones(s, 0);
	for(int i = 0; i < s; i ++) cin >> stones[i];
	Solution *soln = new Solution();
	cout << soln->lastStoneWeight(stones) << nl;
	return;	
}

