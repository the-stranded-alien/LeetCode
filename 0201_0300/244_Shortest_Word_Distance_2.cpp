// Implement the WordDistance class:
// WordDistance(String[] wordsDict) initializes the object with the
// strings array wordsDict.
// int shortest(String word1, String word2) returns the shortest
// distance between word1 and word2 in the array wordsDict.

class WordDistance {
private:
	unordered_map<string, vector<int> > locations;
public:
	WordDistance(vector<string>& wordsDict) {
		for(int i = 0; i < (int) wordsDict.size(); i++) {
			locations[wordsDict[i]].push_back(i);
		}
    }
    
    int shortest(string word1, string word2) {
    	int d = INT_MAX;
    	for(auto i : locations[word1]) {
    		for(auto j : locations[word2]) {
    			d = min(d, abs(i - j));
    		}
    	}
    	return d;
    }
};

