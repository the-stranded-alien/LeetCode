// You are given two strings word1 and word2. Merge the strings by adding
// letters in alternating order, starting with word1. If a string is longer
// than the other, append the additional letters onto the end of the merged
// string. Return the merged string.

class Solution {
public:
    string mergeAlternately(string word1, string word2) {
        int itr1 = 0;
        int itr2 = 0;
        string merged = "";
        while((itr1 < word1.length()) || (itr2 < word2.length())) {
        	if(itr1 < word1.length()) merged += word1[itr1++];
        	if(itr2 < word2.length()) merged += word2[itr2++]; 
        }
        return merged;
    }
};

void solve() {
	// Main Code Goes Here !!
	string word1; cin >> word1;
	string word2; cin >> word2;
	Solution *soln = new Solution();
	string merged = soln->mergeAlternately(word1, word2);
	cout << merged << nl;
	return;
}

