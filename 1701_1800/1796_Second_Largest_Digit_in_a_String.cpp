// Given an alphanumeric string s, return the second largest numerical digit 
// that appears in s, or -1 if it does not exist. An alphanumeric string is a
// string consisting of lowercase English letters and digits.

class Solution {
public:
    int secondHighest(string s) {
		vector<bool> isDigitPresent(10, false);
		int uniqueDigits = 0;
		for(auto ch : s) {
			if((ch >= '0') && (ch <= '9')) {
				int num = ch - '0';
				if(!isDigitPresent[num]) {
					uniqueDigits++;
					isDigitPresent[num] = true;
				}
			}
		}
		if(uniqueDigits <= 1) return -1;
		bool ignoreFirst = false;
		for(int i = 9; i >= 0; i--) {
			if((isDigitPresent[i] == true) && (ignoreFirst == false)) ignoreFirst = true;
			else if((isDigitPresent[i] == true) && (ignoreFirst == true)) return i;
			else continue;
		}
		return -1;
    }
};

void solve() {
	// Main Code Goes Here !!
	string s; cin >> s;
	Solution *soln = new Solution();
	int num = soln->secondHighest(s);
	cout << num << nl;
	return;
}

