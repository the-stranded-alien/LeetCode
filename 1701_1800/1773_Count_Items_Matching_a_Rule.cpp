// You are given an array items, where each items[i] =
// [typei, colori, namei] describes the type, color, and
// name of the ith item. You are also given a rule represented
// by two strings, ruleKey and ruleValue.
// The ith item is said to match the rule if one of the
// following is true:
// ruleKey == "type" and ruleValue == typei.
// ruleKey == "color" and ruleValue == colori.
// ruleKey == "name" and ruleValue == namei.
// Return the number of items that match the given rule.

class Solution {
public:
	int countMatches(vector<vector<string>>& items, string ruleKey, string ruleValue) {
        int matches = 0;
        for(auto item : items) {
        	string type = item[0];
        	string color = item[1];
        	string name = item[2];
        	if(ruleKey == "type" && ruleValue == type) matches++;
        	else if(ruleKey == "color" && ruleValue == color) matches++;
        	else if(ruleKey == "name" && ruleValue == name) matches++;
        }
        return matches;
    }
};

void solve() {
	// Main Code Goes Here !!
	int n; cin >> n;
	vector<vector<string> > items(n, vector<string>(3));
	for(int i = 0; i < n; i++) cin >> items[i][0] >> items[i][1] >> items[i][2];
	string ruleKey; cin >> ruleKey;
	string ruleValue; cin >> ruleValue;
	
	Solution *soln = new Solution();
	cout << soln->countMatches(items, ruleKey, ruleValue);

	return;
}

