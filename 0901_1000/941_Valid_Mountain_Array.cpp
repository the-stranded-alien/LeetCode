// Given an array of integers arr, return true if and
// only if it is a valid mountain array.
// Recall that arr is a mountain array if and only if:
// arr.length >= 3
// There exists some i with 0 < i < arr.length - 1 such that:
// arr[0] < arr[1] < ... < arr[i - 1] < arr[i]
// arr[i] > arr[i + 1] > ... > arr[arr.length - 1]

class Solution {
public:
    bool validMountainArray(vector<int>& arr) {
        int n = arr.size();
        if(n < 3) return false;
        int itr = 0;
        for(itr = 0; itr < (n - 1); itr++) {
        	if(arr[itr] >= arr[itr + 1]) break;
        }
        if(itr == 0 || itr == (n - 1)) return false;
        while(itr < (n - 1)) {
        	if(arr[itr] <= arr[itr + 1]) return false;
        	itr++;
        }
        return true;
    }
};

void solve() {
	// Main Code Goes Here !!
	int n; cin >> n;
	vi arr(n, 0);
	for(int i = 0; i < n; i++) cin >> arr[i];
	Solution *soln = new Solution();
	bool isValidMountain = soln->validMountainArray(arr);
	if(isValidMountain) cout << "true" << nl;
	else cout << "false" << nl;
	return;
}

